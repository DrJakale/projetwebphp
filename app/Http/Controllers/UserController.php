    public function login(){

      $url = 'http://chabanvpn.ovh:8080/api/users/' . $request->email;
      $params = array("http" => array('method' => "GET"));

      $answer = files_get_contents($url,false,stream_context_create($params));
      $jsonarray = json_decode($answer);

      if(!empty($jsonarray[0]->password)){
        if(Hash::check($request->password,$jsonarray[0]->password){
          return view('pages.accueil')->with('ID', $jsonarray[0]->ID);
        }
      }
    }

